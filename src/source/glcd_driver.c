// Current text position
static uint8_t curX = 0;
static uint8_t curPage = 0;

// ===================== DRIVER IMPLEMENTATION =====================
void glcd_init(void)
{
    // Reset pulse
    RST = 0; 
    __delay_ms(10);
    RST = 1;
    __delay_ms(10);

    // Turn off both halves
    glcd_select_chip(CHIP1); glcd_display_off();
    glcd_select_chip(CHIP2); glcd_display_off();

    // Turn on both halves
    glcd_select_chip(CHIP1); glcd_display_on();
    glcd_select_chip(CHIP2); glcd_display_on();
}

void glcd_clear(void)
{
    uint8_t page, col;
    // Left side (CHIP1)
    glcd_select_chip(CHIP1);
    for(page=0; page<8; page++) {
        glcd_set_page(page);
        glcd_set_column(0);
        for(col=0; col<64; col++) {
            glcd_write_data(0x00);
        }
    }
    // Right side (CHIP2)
    glcd_select_chip(CHIP2);
    for(page=0; page<8; page++) {
        glcd_set_page(page);
        glcd_set_column(0);
        for(col=0; col<64; col++) {
            glcd_write_data(0x00);
        }
    }
}

void glcd_select_chip(uint8_t chip)
{
    CS1 = (chip == CHIP1);
    CS2 = (chip == CHIP2);
}

void glcd_write_command(uint8_t cmd)
{
    RS = 0;
    RW = 0;
    GLCD_DATA = cmd;
    glcd_pulse_enable();
}

void glcd_write_data(uint8_t data)
{
    RS = 1;
    RW = 0;
    GLCD_DATA = data;
    glcd_pulse_enable();
}

void glcd_display_on(void)
{
    glcd_write_command(0x3F);
}

void glcd_display_off(void)
{
    glcd_write_command(0x3E);
}

void glcd_set_page(uint8_t page)
{
    glcd_write_command(0xB8 | (page & 0x07));
}

void glcd_set_column(uint8_t col)
{
    glcd_write_command(0x40 | (col & 0x3F));
}

void glcd_pulse_enable(void)
{
    EN = 1;
    __delay_us(2);
    EN = 0;
    __delay_us(2);
}

// ------------------- Bitmap Drawing -------------------
void glcd_draw_bitmap(uint8_t x, uint8_t yPage,
                     uint8_t width, uint8_t height,
                     const uint8_t *bitmap)
{
    uint8_t blocks = height / 8;
    uint16_t index = 0;

    for(uint8_t col = 0; col < width; col++)
    {
        uint8_t realCol = x + col;
        uint8_t chip = (realCol < 64) ? CHIP1 : CHIP2;
        uint8_t col_local = realCol % 64;

        for(uint8_t rowBlock = 0; rowBlock < blocks; rowBlock++)
        {
            glcd_select_chip(chip);
            glcd_set_page(yPage + rowBlock);
            glcd_set_column(col_local);
            glcd_write_data(bitmap[index++]);
        }
    }
}

// ------------------- Text Handling -------------------
void glcd_goto_xy(uint8_t x, uint8_t page)
{
    curX = x;
    curPage = page;
}

void glcd_putc(char c)
{
    // Map non-printable to space
    if(c < 0x20 || c > 0x7E) {
        c = 0x20;
    }
    uint8_t index = c - 0x20;

    // Write the 5 columns of the character
    for(uint8_t i = 0; i < 5; i++) {
        uint8_t chip = (curX < 64) ? CHIP1 : CHIP2;
        uint8_t col_local = curX % 64;
        glcd_select_chip(chip);
        glcd_set_page(curPage);
        glcd_set_column(col_local);
        glcd_write_data(font5x7[index][i]);
        
        // Move to next column
        if(++curX > 127) {
            curX = 0;
            if(++curPage > 7) {
                curPage = 0;
            }
        }
    }

    // One column spacing
    uint8_t chip = (curX < 64) ? CHIP1 : CHIP2;
    uint8_t col_local = curX % 64;
    glcd_select_chip(chip);
    glcd_set_page(curPage);
    glcd_set_column(col_local);
    glcd_write_data(0x00);

    if(++curX > 127) {
        curX = 0;
        if(++curPage > 7) {
            curPage = 0;
        }
    }
}

void glcd_puts(const char *str)
{
    while(*str) {
        glcd_putc(*str++);
    }
}
